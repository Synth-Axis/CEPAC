					<strong class="title-strong">CRIANÇAS E JOVENS NÃO ACOMPANHADOS(AS)</strong>
                    <br><br>
					Foi criada uma plataforma para registo de casos de menores não acompanhados, que permite ainda o levantamento de disponibilidades de acolhimento temporário, bem como a identificação de ações voluntárias de transporte para território nacional.
                    <br><br>
					De momento, pode contactar esta plataforma através do e-mail <a href="mailto:childcare.ukraine@seg-social.pt"><strong>childcare.ukraine@seg-social.pt</strong></a>, e da linha telefónica de apoio <strong>300 511 490</strong>, todos os dias da semana das 8h às 20h.
                    <br><br>
					<b>É essencial reforçar a importância da identificação de todas as crianças que se encontrem nesta situação, quer estejam já em Portugal ou em trânsito, a fim de garantir a sua segurança e plena proteção, bem como o acesso a todos as valências de apoio existentes no país.</b>

