			<div class="middleparagraph">
				<p>Portugal criou um programa especial de proteção a cidadãos ucranianos provindos do conflito, que consiste na atribuição de uma Autorização de Residência ao abrigo do regime de proteção temporária, o qual garante a atribuição a cada cidadão ou cidadã dos seguintes identificadores:
                    <br><br>
					- Número de Identificação Fiscal (NIF);
                    <br>
					- Número de Identificação da Segurança Social (NISS);
                    <br>
					- Número de Utente do Serviço Nacional de Saúde (NUS).
                    <br><br><br>
					&nbsp;

					<strong class="title-strong">O regime de proteção temporária é aplicável a cidadãos e cidadãs:</strong>
                    <br><br>
					- nacionais da Ucrânia e aos cidadãos estrangeiros de outras nacionalidades ou apátridas beneficiários de proteção internacional na Ucrânia, provenientes desse país, não podendo ali voltar, em consequência da situação de guerra que aí ocorre;
                    <br>
					- estrangeiros(as) de outras nacionalidades ou apátridas que se encontrem nas circunstâncias acima descritas e que comprovem ser familiares, designadamente parentes, afins, cônjuges ou unidos de facto dos cidadãos referidos no número anterior;
                    <br>
					- residentes permanentes na Ucrânia, ou que tenham uma autorização de residência temporária;
                    <br>
					- que beneficiem de um visto de longa duração destinado à obtenção deste tipo de autorização e cujo regresso seguro e duradouro ao seu país de origem não seja possível.
                    <br><br>

					<strong class="title-strong">PEDIDO DE PROTEÇÃO TEMPORÁRIA</strong>
                    <br><br>
					O Serviço de Estrangeiros e Fronteiras disponibiliza uma <b>plataforma online trilingue (versões ucraniana, inglesa e portuguesa)</b> que possibilita fazer online um pedido de proteção temporária de um ano, prorrogável por dois períodos de seis meses: <a href="https://sefforukraine.sef.pt/"><strong>SEFforUkraine.</strong><strong>sef</strong><strong>.pt</strong> </a>

                    <br><br>

					Esta plataforma pode ser utilizada apenas por cidadãos e cidadãs maiores de 18 anos de idade. Os(as) menores, pela sua vulnerabilidade, necessitam de confirmar a filiação ou realizar o registo dos pedidos de proteção temporária num dos <a href="https://eportugal.gov.pt/noticias/balcoes-de-atendimento-do-sef-exclusivos-para-cidadaos-ucranianos">24 balcões de atendimento do SEF exclusivos para o efeito</a>.

                        <br><br>

					Para esclarecimentos adicionais contactar: <a href="mailto:SEFforUkraine@sef.pt">SEFforUkraine@sef.pt</a>

